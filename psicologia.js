function createPsychologistCard(psychologist) {
    const specializationTags = psychologist.specialization.map(spec =>
        `<span class="specialization-tag ${spec.color}">${spec.text}</span>`
    ).join('');

    return `
    <div class="psychologist-card">
        <img src="${psychologist.image}" alt="${psychologist.name}" class="w-full h-56 sm:h-64 md:h-72 object-cover">
        
        <div class="psychologist-info">
            <h3 class="text-xl sm:text-2xl font-semibold mb-2">${psychologist.name}</h3>
            <p class="catholic-phrase mb-4">"${psychologist.phrase}"</p>
        
            <div class="split-button mb-4">
                <button onclick="openModal('aboutme-modal', '${psychologist.name}')" class="bg-blue-500 text-white hover:bg-blue-600">Sobre Mí</button>
                <button onclick="openModal('details-modal', '${psychologist.name}')" class="bg-green-500 text-white hover:bg-green-600">Detalles</button>
            </div>

            <div class="flex flex-wrap justify-start mt-6">
                ${specializationTags}
            </div>
        </div>
    </div>
    `;
}

function renderPsychologists() {
    const container = document.getElementById('psychologists-container');
    container.innerHTML = psychologists.map(createPsychologistCard).join('');
}

function openModal(modalId, name) {
    const psychologist = psychologists.find(p => p.name === name);
    const modal = document.getElementById(modalId);

    if (modalId === 'aboutme-modal') {
        document.getElementById('aboutme-modal-name').textContent = psychologist.name;
        document.getElementById('aboutme-modal-image').src = psychologist.image;
        document.getElementById('aboutme-modal-image').alt = psychologist.name;
        
        document.getElementById('aboutme-modal-cedula').innerHTML = psychologist.cedula && psychologist.cedula !== "En Proceso" 
        ? `<strong class="font-semibold">Cédula:</strong> ${psychologist.cedula}`
        : '';

        document.getElementById('aboutme-modal-description').innerHTML = `${psychologist.description}`;
    } else if (modalId === 'details-modal') {
        document.getElementById('details-modal-name').textContent = psychologist.name;

        const diplomasList = document.querySelector('#details-modal-diplomas ul');
        diplomasList.innerHTML = psychologist.diplomas.map(diploma => `<li class="mb-1">${diploma}</li>`).join('');
    }

    const whatsappButton = document.getElementById(`${modalId}-whatsapp`);
    whatsappButton.href = `https://wa.me/523112185228?text=Información para agendar una cita psicológica con ${psychologist.name}.`;

    modal.style.display = 'flex';
    document.body.classList.add('modal-open');
}

function closeModal(modalId) {
    const modal = document.getElementById(modalId);
    modal.style.display = 'none';
    document.body.classList.remove('modal-open');
}

// Cerrar el modal al hacer clic fuera del contenido
document.querySelectorAll('.modal').forEach(modal => {
    modal.addEventListener('click', function(event) {
        if (event.target === this) {
            closeModal(this.id);
        }
    });
});

renderPsychologists();
